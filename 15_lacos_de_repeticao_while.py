# Irá executar um bloco de código várias vezes, até que houver uma parada
# while  == enquanto
# faz um looping infinito
# debug é colocar um breakpoint (parar para entender)
#Continue para a execução ali e retorna no inicio do laço

i = 0

while i < 10:
    print(i)
    break     #interrompe o laço
    i = i + 1


